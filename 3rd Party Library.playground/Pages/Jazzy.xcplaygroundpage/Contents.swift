 # Jazzy Documentation for Xcode
 
 ## How to install it
 
 From a terminal issue the following command:
 
 `sudo gem install jazzy`
 
 Go to your root project drirectory:
 
 `cd [project folder]`
 
 ## How to use it

 Check for available command options
 
 jazzy --help

 
 - example: *Running `jazzy` with most common options:*\
 \
    ` jazzy --theme apple --hide-documentation-coverage --author "Gaetano Causio" --module "TurkinoFoundation" --author_url "https://www.gaetanocausio.com" --github_url "https://github.com/gaetanocausio/TurkinoFoundation"`
 

