 # Arduino
 
 ## Initial Set Up & Test
 
 - Connect USB cable from the Mac to the Arduino board
 - Start Arduino application and load a sketch via **File > Examples > 1.Basics > Blink**
 - Select correct board via **Tools > Board** menu 
 - Select correct serial port via **Tools > Port**
 - Upload sketch to Arduino via **Sketch > Upload**
 
 
 
